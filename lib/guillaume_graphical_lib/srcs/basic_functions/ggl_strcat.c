#include "ggl.h"

char	*ggl_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	j;

	i = ggl_strlen(s1);
	j = 0;
	while (s2[j])
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
